/**
 * 본문에 삽입되는 원문(관리자 직접 작성, 향후 AI/PDF 추출 텍스트 포함)이
 * MDX 파싱을 깨뜨리지 않도록 최소한의 안전 이스케이프만 적용합니다.
 * 의도적인 마크다운 서식(굵게, 링크 등)은 건드리지 않고, 실제로 MDX 구조를
 * 깨뜨릴 수 있는 두 가지 케이스만 처리합니다.
 *
 * 1. 줄 맨 앞의 '#' — rehypeSectionCards가 h2를 기준으로 섹션을 나누기 때문에,
 *    본문 중간에 등장한 '#'이 새 섹션 제목으로 오인되는 것을 막습니다.
 * 2. '{' / '}' — MDX는 중괄호를 JS 표현식으로 해석하므로, 리터럴 중괄호가
 *    포함된 텍스트(수치·단위 표기 등)가 그대로 들어오면 컴파일이 실패합니다.
 *    `{"{"}` 형태(문자열 리터럴을 담은 JSX 표현식)로 감싸 리터럴 문자로 되돌립니다.
 */
export function escapeMdxBody(text: string): string {
  return text
    .split("\n")
    .map((line) => (/^\s*#/.test(line) ? line.replace(/^(\s*)#/, "$1\\#") : line))
    .join("\n")
    // '{'와 '}'를 한 번의 패스로 함께 치환합니다. 순차적으로 .replace()를 두 번
    // 호출하면 첫 번째 치환 결과물({"{"} 안의 '}' 등)이 두 번째 치환에서 다시
    // 매칭되어 이중으로 이스케이프되는 문제가 있었습니다.
    .replace(/[{}]/g, (ch) => (ch === "{" ? '{"{"}' : '{"}"}'));
}

/** JSX 속성값(큰따옴표로 감싸는 문자열) 안에 안전하게 넣기 위한 이스케이프입니다. */
export function escapeAttribute(text: string): string {
  return text.replace(/"/g, "&quot;");
}
